  WorhpInternal::~WorhpInternal(){
    if (worhp_p_.initialised || worhp_o_.initialised || worhp_w_.initialised || worhp_c_.initialised)
      WorhpFree(&worhp_o_, &worhp_w_, &worhp_p_, &worhp_c_);
  }

